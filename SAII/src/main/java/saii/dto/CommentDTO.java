package saii.dto;

public class CommentDTO {
	private String cmt_no;
	private String cmt_id;
	private String cmt_regdate;
	private String cmt_content;
	private String board_no;
	private String nickname;

	public String getCmt_no() {
		return cmt_no;
	}
	public void setCmt_no(String cmt_no) {
		this.cmt_no = cmt_no;
	}
	public String getCmt_id() {
		return cmt_id;
	}
	public void setCmt_id(String cmt_id) {
		this.cmt_id = cmt_id;
	}
	public String getCmt_regdate() {
		return cmt_regdate;
	}
	public void setCmt_regdate(String cmt_regdate) {
		this.cmt_regdate = cmt_regdate;
	}
	public String getCmt_content() {
		return cmt_content;
	}
	public void setCmt_content(String cmt_content) {
		this.cmt_content = cmt_content;
	}
	public String getBoard_no() {
		return board_no;
	}
	public void setBoard_no(String board_no) {
		this.board_no = board_no;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	
	
	
}

